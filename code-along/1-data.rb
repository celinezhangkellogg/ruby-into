# In Ruby, there are different *types* of data:

# Numbers
puts 5
puts 2

puts 5 + 2
puts 5 / 2.0
puts 5 / 2

# Non-numbers
puts "hello world"
puts "tacos are" + " delish"
puts "tacos" * 3
puts "tacos" + 3.to_s


# True, False
puts True
puts False
puts 3 == 2

# Nothing

# Variables

x = 5
y = 2

puts x + y

food = "tacos"
quantity = 3
puts food * quantity


# Combine strings and variables
puts "I want #{quantity} #{food}"

name = "Ben"
puts "hello #{name}"

